public class ExemploFor {
  public static void main(String[] args) {
    for (int carneirinhos = 1; carneirinhos <= 20; carneirinhos++) {// primeiro a variável carneirinhos do tipo inteiro
                                                                    // se inicializa com o valor 1, após o ponto ; é
                                                                    // verificado se o número é menor ou igual a 20,
                                                                    // caso seja o valor será incrementado em +1 até que
                                                                    // a condição se torne falsa
      System.out.println("Contando carneirinhos : " + carneirinhos);
    }
  }
}
